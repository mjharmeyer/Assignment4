    hard_target = df_hard.columns[-1] if len(df_hard.columns) > 2 else None

    # Custom algorithm for each two dimensional dataset
    analyze_dataset_custom(df_easy, easy_features, easy_target, k=2, dataset_name="TwoDimEasy")
    analyze_dataset_custom(df_easy, easy_features, easy_target, k=3, dataset_name="TwoDimEasy")
    analyze_dataset_custom(df_hard, hard_features, hard_target, k=4, dataset_name="TwoDimHard")
    analyze_dataset_custom(df_hard, hard_features, hard_target, k=3, dataset_name="TwoDimHard")
    wine_features = [col for col in df_wine.columns if col != 'quality']
    scaler = StandardScaler()
    wine_scaled_data = scaler.fit_transform(df_wine[wine_features])

    print("\nAnalysis for Wine Dataset:")
    k_values = [2, 4, 6, 8, 10]
    for k in k_values:
        # Using sklearn here to easily extract SSE for quick comparison
        km = KMeans(n_clusters=k, random_state=42)
        preds = km.fit_predict(wine_scaled_data)
        score = silhouette_score(wine_scaled_data, preds)
        print(f"Wine Dataset (k={k}) -> Inertia (SSE): {km.inertia_:.2f}, Silhouette Score: {score:.3f}")

    # Validation against Quality
    km_best = KMeans(n_clusters=6, random_state=42)
    wine_preds = km_best.fit_predict(wine_scaled_data)
