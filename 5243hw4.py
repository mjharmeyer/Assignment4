    print(pd.crosstab(df_wine['quality'], wine_preds, rownames=['Actual Quality'], colnames=['Assigned Cluster']))

    # Run off the shelf algorithms
    print("\nOff-the-Shelf Clustering Comparisons:")
    km_lbl, db_lbl, agg_lbl = run_off_the_shelf(df_easy, easy_features, n_clusters=2, eps=0.5)
